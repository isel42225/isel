package serie1;

public class WordsOnFile {
    private  String word ;
    private  int nChars;

    public WordsOnFile (String word, int nChars){
        this.word = word;
        this.nChars = nChars;


    }

    public String getWord() {
        return word;
    }

    public int getNchars() {
        return nChars;
    }
}
